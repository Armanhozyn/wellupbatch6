<?php

class Calculator{


    public function add($num_one,$num_two){
        return $num_one + $num_two;
    }

    public function subs($num_one,$num_two){
        return $num_one - $num_two;
    }

    public function multi($num_one,$num_two){
        return $num_one * $num_two;
    }

    public function divide($num_one,$num_two){
        return $num_one / $num_two;
    }
}
